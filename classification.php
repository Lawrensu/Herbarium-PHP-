                    <h1>Brief Explanation</h1>
                    <ul>   
                        <li>Genus of plants in the family Dipterocarpaceae. It contains some 113 species, distributed from Sri Lanka and southern India to the Andaman Islands, Myanmar, southern China, and southward throughout Malesia to New Guinea. </li>
                        <li>They are mainly main and subcanopy trees of lowland rainforest,[1] but some species can become also emergent trees, such as Hopea nutans.</li>
                        <li>The genus was named after John Hope, the first Regius Keeperof the Royal Botanic Garden, Edinburgh</li>
                        <li>Trees in this genus are small or medium, but they can grow occasionally large. They have low branches and sometimes thick buttress roots. Their bark is chocolate brown and smooth at first, but later crack and mottle with grey spots.</li>
                    
