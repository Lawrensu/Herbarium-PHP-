            <p>After 24 hours, observe plants in a press, replace parchment if wrinkling, and reassemble specimens if needed. Avoid removing paper from delicately petaled specimens. Reapply pressure and leave plants in the press for 5 days before checking again. Most plants need a week to dry, while fleshier plants may take up to two weeks.</p>
          </article>
