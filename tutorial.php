      <!--Definition List-->
      <dl>
        <dt>Herbarium</dt>
        <dd>A collection of preserved plant specimens, often pressed and mounted on sheets of paper.</dd>
        <dt>Pressing</dt>
        <dd>Flattening and drying plant specimens to preserve them for future reference.</dd>
        <dt>Mounting</dt>
        <dd>Attaching pressed plants to sheets of paper for storage and display.</dd>
        <dt>Identification</dt>
        <dd>Assigning a name to a plant specimen based on its characteristics.</dd>
        <dt>Cataloging</dt>
        <dd>Recording information about plant specimens for easy retrieval and reference.</dd>
      </dl>

