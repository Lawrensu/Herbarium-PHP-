            <p>Load plants into a press with parchment between them and plywood dividers. Apply pressure to keep plants flat while drying. Use a simple plant press with wingnuts for adjustable pressure. A stack of heavy books can work, but lacks fine-tuning for successful pressing.</p>
          </article>

          <!-- Step 5 continued -->
          <article class="step">
            <h3>Step 5: Pressing The Specimens (continued)</h3>
